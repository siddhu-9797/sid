import { useState, useEffect } from "react";
import { Navbar, Nav, Container, NavbarBrand } from "react-bootstrap";
import sid from '../assets/img/sid.jpg';
import navIcon1 from '../assets/img/nav-icon1.svg';
import navIcon2 from '../assets/img/nav-icon2.svg';
import navIcon3 from '../assets/img/nav-icon3.svg';
import { HashLink } from 'react-router-hash-link';
import {
  BrowserRouter as Router
} from "react-router-dom";

export const NavBar = () => {

  const [activeLink, setActiveLink] = useState('home');
  const [scrolled, setScrolled] = useState(false);

  useEffect(() => {
    const onScroll = () => {
      if (window.scrollY > 50) {
        setScrolled(true);
      } else {
        setScrolled(false);
      }
    }

    window.addEventListener("scroll", onScroll);

    return () => window.removeEventListener("scroll", onScroll);
  }, [])

  const onUpdateActiveLink = (value) => {
    setActiveLink(value);
  }

  return (
    <Router>
      <Navbar expand="md" className={scrolled ? "scrolled" : ""}>
        <Container>
        <img 
              src={sid} 
              alt="Profile Picture" 
              className="profile-pic" 
              circ
              style={{
                borderRadius: "50%",
                width: 80,
                height: 80,
                background: "",
                display: "block",
                marginRight: "2rem",
              }}
            />
        <Navbar.Brand href="/" style={{ fontSize: '2.5rem', fontWeight: 'bold', color: '#ffff' }}>
            Sid Goparaju
          </Navbar.Brand>
          <Navbar.Toggle aria-controls="basic-navbar-nav">
            <span className="navbar-toggler-icon"></span>
          </Navbar.Toggle>
          <Navbar.Collapse id="basic-navbar-nav">
            <Nav className="ms-auto">
              <Nav.Link href="#home" className={activeLink === 'home' ? 'active navbar-link' : 'navbar-link'} onClick={() => onUpdateActiveLink('home')}>Home</Nav.Link>
              <Nav.Link href="#skills" className={activeLink === 'skills' ? 'active navbar-link' : 'navbar-link'} onClick={() => onUpdateActiveLink('skills')}>Background</Nav.Link>
              <Nav.Link href="#projects" className={activeLink === 'projects' ? 'active navbar-link' : 'navbar-link'} onClick={() => onUpdateActiveLink('projects')}>Projects</Nav.Link>
            </Nav>
            <span className="navbar-text">
              <div className="social-icon">
                <a href="#"><img src={navIcon1} alt="https://linkedin.com/in/sidgoparaju" target="_blank"/></a>
              </div>
              <HashLink to='#connect'>
                <button className="vvd"><span>Letâ€™s Connect</span></button>
              </HashLink>
            </span>
            
          </Navbar.Collapse>
        </Container>
      </Navbar>
    </Router>
  )
}
